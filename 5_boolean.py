#Tipo de dado bool (boolean) introdução aos tipos de dados

#Boolean é um tipo de dado que representa um valor lógico
#Ao questionar algo em um programa
#Só existem duas respostas possiveis
#True (Verdadeiro) ou False (Falso)
#True e False são palavras reservadas do Python
#Existem varios operadores que retornam um valor booleano
#Dentre eles o ==, que é o operador logico que
#Questiona se um valor é igual a outro

print(10 == 10)  # True, pois 10 é igual a 10
print(10 == 11)  # False, pois 10 não é igual a
print(type(10 == 10))  # <class 'bool'>, o tipo do resultado é booleano
print(type(10 == 11))  # <class 'bool'>, o tipo do resultado é booleano