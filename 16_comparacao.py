#Operadore de comparação (relacionais)
# OP    SIGNIFICADO   Exemplo(true)
#>      maior         5 > 2
#>=     maior ou igual 5 >= 5
#<      menor         2 < 5
#<=     menor ou igual 2 <= 5
#==     igual         5 == 5
#!=     diferente    5 != 2

maior = 5 > 2
maior_ou_igual = 5 >= 10
menor = 2 < 5
menor_ou_igual = 2 <= 5
igual = 5 == 5
diferente = 5 != 2
print(maior)
print(maior_ou_igual)
print(menor)
print(menor_ou_igual)
print(igual)
print(diferente)