#Concatenaçao(+) e repetição(*) com operadores artiméticos
# A concatenação de strings é feita com o operador +
# A repetição de strings é feita com o operador *
# A concatenação une duas ou mais strings em uma única string
# A repetição cria uma nova string repetindo a original um número específico de vezes
# Exemplo de concatenação e repetição de strings


concatenacao = 'A' + 'B' + 'C'  # Concatenando strings
print(concatenacao)  # Resultado: 'ABC'

a_dez_vezes = 'A' * 10  # Repetindo a string 'A' 10 vezes
tres_vezes_luiz = 3 * 'Luiz'  # Repetindo a string 'Luiz' 3 vezes
print(a_dez_vezes)  # Resultado: 'AAAAAAAAAA'
print(tres_vezes_luiz)  # Resultado: 'LuizLuiz

