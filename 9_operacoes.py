#Introdução às operações aritméticas em Python
#Operadores aritméticos básicos: +, -, *, /, //, **, %
#Operadores aritméticos são usados para realizar cálculos matemáticos
#Exemplos de operações aritméticas:

adicao = 10+10 #int
print('Adição:', adicao)  # Imprime o resultado da adição

subtracao = 20-5 #int
print('Subtração:', subtracao)  # Imprime o resultado da subtra

multiplicacao = 5*4 #int
print('Multiplicação:', multiplicacao)  # Imprime o resultado da multiplicação

divisao = 20/4 #float
print('Divisão:', divisao)  # Imprime o resultado da divisão

divisao_inteira = 10 // 3 #float
print('Divisão Inteira:', divisao_inteira)  # Imprime o resultado da divisão inteira

exponenciacao = 2 ** 10 #int
print('Exponenciação:', exponenciacao)  # Imprime o resultado da exponenciação 

modulo = 55 % 2 #Resto de divisão
print('Módulo:', modulo)  # Imprime o resultado do módulo