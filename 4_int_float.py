# Tipos int e float(numeros) introdução aos tipos de dados


# int -> Numero inteiro
# O tipo int representa qualquer numero inteiro, positivo ou negativo, sem casas decimais.
print (11) #int
print (-11) #int negativo
print (0) #int zero

#float -> Numero com ponto flutuante
#O Tipo float representa qualquer numero
# Positivo ou negativo com ponto flutuante, ou seja, com casas decimais.
#Float sem sinal é considerado positivo
print (1.1, 10.1) #float positivo
print (-1.1, -10.1) #float negativo
print (0.0) #float zero

# A Função type mostra o tipo que o Pythoninferiu ao valor

print(type('Vinicius'))
print(type(11))  # int
print(type(1.1))  # float  