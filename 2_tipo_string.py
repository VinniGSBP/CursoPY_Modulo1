#Argumento é um algo que é passado para uma função
# \r \n -> CRLF (Padrao quebra de linha no Windows)
# \n -> LF (Padrao quebra de linha no Linux, mac, unix)
# end='' -> acrescenta o final da linha, ou seja, o que será impresso no final, sendo caracter ou quebra de linha
# sep='' -> Não coloca espaço entre os valores impressos


print(12, 34, sep="-", end='\r\n')  # Imprime múltiplos valores separados por vírgula
print(54, 6, sep='=', end='\n')  # Imprime múltiplos valores separados por vírgula

