#Sobre PRINT e STR

"""
Python = Linguagem de programaçao
Tipo de tipagem = Dinamica / Forte
str -> string -> texto
String sao textos, que estão dentro de aspas simples ou duplas
"""
# Aspas Simples
print ('Olá, Mundo!')  # Imprime uma string com aspas simples
# Aspas Duplas
print ("Olá, Mundo!")  # Imprime uma string com aspas duplas
# Escape
print("Vinicius \"Gabriel\"") 
# r
print(r"Vinicius \"Gabriel\"") 
#Print formatado para nao utilizar escape ou r, podendo ser iniciado com aspas simples ou duplas
print('Vinicius "Gabriel"')
print("Vinicius 'Gabriel'") 


